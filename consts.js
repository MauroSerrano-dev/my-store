export function convertDolarToCurrency(value, currency) {
    if (currency === 'usd')
        return value
    if (currency === 'gbp')
        return Math.round(value * 0.82)
    if (currency === 'eur')
        return Math.round(value * 0.94)
    if (currency === 'brl')
        return Math.round(value * 4.87)
    console.error('Currency not found')
    return null
}

